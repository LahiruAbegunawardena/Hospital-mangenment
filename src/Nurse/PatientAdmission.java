    private void srchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srchActionPerformed
        // TODO add your handling code here:
        try {
            String pId="", pNm="", cnfm_dctId="", ac_Id="", wdID="";
            Date a_Dt= new Date(2017-01-01), lvDt=new Date(2017-01-01);         
            boolean check_1=true;
            
            Statement stat = Admin.DataBase.getStatement();
            ResultSet rs1 = stat.executeQuery("SELECT * FROM Patient WHERE nic_no='"+p_nic.getText()+"'");
            
            if(rs1.next()){
                pId = rs1.getString("patient_id");
                pNm = rs1.getString("patient_fname")+" "+rs1.getString("patient_lname");
                cnfm_dctId = rs1.getString("confirm_doct_id");
                ac_Id = rs1.getString("assigned_consult_id");                                
            }
            else{
                check_1 = false;
                JOptionPane.showMessageDialog(null, "This patient is not registered", "Error...",JOptionPane.INFORMATION_MESSAGE);
            }                       
            
            if(check_1){
                ResultSet rs2 = stat.executeQuery("SELECT * FROM Admission WHERE patient_id='"+pId+"'");
                if(rs2.next()){
                    wdID = rs2.getString("ward_id");
                    a_Dt = rs2.getDate("admit_dt");
                    lvDt = rs2.getDate("leave_dt");                    
                }
            }
            p_id.setText(pId);
            p_nm.setText(pNm);
            admt_dt.setDate(a_Dt);
            wd_id.setText(wdID);
            lv_dt.setDate(lvDt);
            dct_id.setText(cnfm_dctId);
            cnslt_id.setText(ac_Id);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_srchActionPerformed

    private void dltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dltActionPerformed
        // TODO add your handling code here:
        boolean check=true, check2=true;
        String pID = "", admtID="";
        try {
            Statement stat = Admin.DataBase.getStatement();
            ResultSet rs1 = stat.executeQuery("SELECT * FROM Patient WHERE nic_no='"+p_nic.getText()+"'");
            if(rs1.next()){
                pID = rs1.getString("patient_id");
            }else{
                JOptionPane.showMessageDialog(null, "There is no patient with this nic no..", "Error...",JOptionPane.INFORMATION_MESSAGE);
                check=false;
            }
            
            if(check){
                ResultSet rs2 = stat.executeQuery("SELECT admt_id From Admission WHERE patient_id='"+pID+"'");
                if(rs2.next()){
                    admtID = rs2.getString(1);
                }else{
                    check2 = false;
                    JOptionPane.showMessageDialog(null, "This patient hasn't admitted yet..", "Error...",JOptionPane.INFORMATION_MESSAGE);
                }
                if(check2){
                    stat.executeUpdate("DELETE FROM Admission WHERE admt_id='"+admtID+"'");
                    JOptionPane.showMessageDialog(null, "Succesfully deleted that Admission", "Deletion...",JOptionPane.INFORMATION_MESSAGE);
                }
                
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_dltActionPerformed

    private void updtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updtActionPerformed
        // TODO add your handling code here:
        String nic= p_nic.getText();
        boolean chck=true;
        try {
            int wrd = Integer.valueOf(wd_id.getText());
        } catch (NumberFormatException e) {
            chck = false;
            JOptionPane.showMessageDialog(null, "Invalid data type..", "Submission...",JOptionPane.INFORMATION_MESSAGE);
        }
        
        if(chck && nic.equals("")){
            JOptionPane.showMessageDialog(null, "You didn't enter patient nic..", "Validation error..",JOptionPane.INFORMATION_MESSAGE);
        }else{
            try {
                boolean check=false;
                Statement stat = DataBase.getStatement();
                String pID="", admtID="";
                ResultSet rs1 = stat.executeQuery("SELECT * FROM Patient WHERE nic_no='"+nic+"'");

                if(rs1.next()){
                    check=true;
                    pID = rs1.getString("patient_id");
                }
                else{                
                    JOptionPane.showMessageDialog(null, "This patient is not registered.. \n Can't update...", "Error...",JOptionPane.INFORMATION_MESSAGE);
                }

                if(check){
                    ResultSet rs2 = stat.executeQuery("SELECT * FROM Admission WHERE patient_id="+pID);
                    boolean check2 = false;
                    if(rs2.next()){
                        admtID = rs2.getString("admt_id");
                        check2=true;
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "This patient is not a admitted one.. \n can't update... ", "Error...",JOptionPane.INFORMATION_MESSAGE);
                    }
                    if(check2){
                        DateFormat dfm = new SimpleDateFormat("yyyy-MM-dd");
                        
                        String dy2 = dfm.format(lv_dt.getDate());
                        stat.executeUpdate("UPDATE Admission "
                            + "SET ward_id='"+wd_id.getText()+"', leave_dt='"+dy2+"' "
                            + "WHERE admt_id='"+admtID+"'");
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

            p_nic.setText("");
            wd_id.setText("");
            p_nm.setText("");
            p_id.setText("");
            dct_id.setText("");
            cnslt_id.setText("");
            admt_dt.setDateFormatString("");
            lv_dt.setDateFormatString("");
        }
    }//GEN-LAST:event_updtActionPerformed

