    private void sbmtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sbmtActionPerformed
        // TODO add your handling code here:
        boolean chck=true;
        try {
            int wrd = Integer.valueOf(wd_id.getText());
        } catch (NumberFormatException e) {
            chck = false;
            JOptionPane.showMessageDialog(null, "Invalid data type..", "Submission...",JOptionPane.INFORMATION_MESSAGE);
        }
        
        if((chck) && p_nic.equals("")){
            JOptionPane.showMessageDialog(null, "You didn't enter patient nic..", "Validation error..",JOptionPane.INFORMATION_MESSAGE);
        }else{
            try {
                String p_id="", cnfm_doctId="";
                boolean check=true;
                Statement stat = DataBase.getStatement();
                ResultSet rs1 = stat.executeQuery("SELECT * FROM Patient WHERE nic_no='"+p_nic.getText()+"'");

                if(rs1.next()){                
                    p_id=rs1.getString("patient_id");
                    cnfm_doctId = rs1.getString("confirm_doct_id");
                }
                else{
                    JOptionPane.showMessageDialog(null, "This patient is not a registered one \n Register through Admin...", "Error..",JOptionPane.INFORMATION_MESSAGE);
                    check=false;                
                }
                if(check){
                    DateFormat dfm = new SimpleDateFormat("yyyy-MM-dd");
                    String dy1 = dfm.format(admt_dt.getDate());
                    //String dy2 = dfm.format(lv_dt.getDate());
                    stat.executeUpdate("INSERT INTO Admission(patient_id, ward_id, cnfm_doct_id, admit_dt) "
                            + "VALUES('"+p_id+"','"+wd_id.getText()+"','"+cnfm_doctId+"','"+dy1+"')");

                }                        
            } catch (Exception e) {
                e.printStackTrace();
            }

            p_nic.setText("");
            wd_id.setText("");
            admt_dt.setDateFormatString("");
        }
    }//GEN-LAST:event_sbmtActionPerformed

