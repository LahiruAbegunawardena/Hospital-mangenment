    private void sbmitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sbmitBtnActionPerformed
        // TODO add your handling code here
        String wrdNM=wd_nm.getText();
        int bdno=0;
        boolean checkValidity=true;
        if(wrdNM.equals("")){
            checkValidity=false;
        }
        if(checkValidity){
            try {
                bdno = Integer.valueOf(bd_no.getText());
                Statement stat = DataBase.getStatement();
                stat.executeUpdate("INSERT INTO Ward (ward_name, head_doct_name, no_of_beds, description) "
                        + "VALUES('"+wrdNM+"','"+hdoct_nm.getText()+"','"+bdno+"','"+desc.getText()+"')");
            } catch(NumberFormatException e1){
                JOptionPane.showMessageDialog(null, "Invalid data type..", "Submission...",JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }
            JOptionPane.showMessageDialog(null, "Successfully added new Ward..", "Notice..",JOptionPane.INFORMATION_MESSAGE);

            wd_nm.setText("");
            wd_id.setText("");
            hdoct_nm.setText("");
            bd_no.setText("");
            desc.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "You have input data in wrong pattern..", "Validation Error..",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_sbmitBtnActionPerformed

    private void srch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srch1ActionPerformed
        // TODO add your handling code here:
        String wdno="", bed_no="", dscrpt="", hdDoctno="";
        try {
            Statement stat = DataBase.getStatement();
            ResultSet rs1 = stat.executeQuery("SELECT * FROM Ward WHERE ward_name='"+wd_nm.getText()+"'");
            
            if(!(rs1.next())){
                JOptionPane.showMessageDialog(null, "Ward name you entered is wrong...", "Error..",JOptionPane.INFORMATION_MESSAGE);
                wd_nm.setText("");
            }
            else{
                wdno=rs1.getString("ward_id");
                bed_no = rs1.getString("no_of_beds");
                dscrpt = rs1.getString("description");
                hdDoctno = rs1.getString("head_doct_name");
                                
                wd_id.setText(wdno);
                hdoct_nm.setText(hdDoctno);
                bd_no.setText(bed_no);
                desc.setText(dscrpt);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }            
    }//GEN-LAST:event_srch1ActionPerformed

