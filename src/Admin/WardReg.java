    private void updtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updtActionPerformed
        // TODO add your handling code here:
        try {
            Statement stat = DataBase.getStatement();
            ResultSet rs1 = stat.executeQuery("SELECT * FROM Ward WHERE ward_name='"+wd_nm.getText()+"'");
            
            boolean check=true;
            String wardId = "";
            int bdno= Integer.valueOf(bd_no.getText());
            
            if(rs1.next()){
                wardId = rs1.getString("ward_id");
            }
            else if(!(rs1.next())){
                JOptionPane.showMessageDialog(null, "There is no query in table in that name cannot update..", "Error..",JOptionPane.INFORMATION_MESSAGE);
                check=false;
            }
            
            if(check){
                stat.executeUpdate("UPDATE Ward "
                        +         "SET ward_name='"+wd_nm.getText()+"', head_doct_name='"+hdoct_nm.getText()+"', no_of_beds='"+bdno+"', description='" +desc.getText()
                        +         "' WHERE ward_id='"+wardId+"'");
                JOptionPane.showMessageDialog(null, "Succesfully updated...", "Update..",JOptionPane.INFORMATION_MESSAGE);
            }
                                    
            wd_nm.setText("");
            wd_id.setText("");
            hdoct_nm.setText("");
            bd_no.setText("");
            desc.setText("");
                        
        } catch(NumberFormatException e1){
                JOptionPane.showMessageDialog(null, "Invalid data type..", "Error...",JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_updtActionPerformed

